function getHexaColor(color) {
    if (color=="white") {
        return console.log("#FFFFFF")
    } else if (color=="black") {
        return console.log("#000000")
    } else if (color=="blue") {
        return console.log("#0b24fb")
    } else if (color=="green") {
        return console.log("#0e7e12")
    } else if (color=="yellow") {
        return console.log("#fffd38")
    } else if (color=="pink") {
        return console.log("#fec1cc")
    } 
}
getHexaColor("blue");
